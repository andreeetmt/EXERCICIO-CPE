#include <iostream>
using namespace std;

int main() 
{
    double resx = 7 + 3 * 6 / 2 - 1; // resposta de x
    double resy = 2 % 2 + 2 * 2 / 2; // resposta de y
    double resz = 3 * 9 * 3 + 9 * 3 / 3 / 3; // resposta de z

    cout << "respostas: " << "x " << resx  << " y " << resy << " z " << resz << endl; 
    return 0;
}
